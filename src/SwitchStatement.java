public class SwitchStatement {
    static void main(String[] args)
    {
        String day = "Friday";
        switch (day)
        {
            case "Sunday" :
                System.out.println("Its Sunday");
                break;
            case "Monday" :
                System.out.println("Its Monnday");
                break;
            case "Friday" :
                System.out.println("Its Friday");
                break;
        }
    }
}
